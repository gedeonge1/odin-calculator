//function that takes two argument at time and return the result
function add(num1,num2){
    num1 += num2
    return num1
   }
function substract(num1,num2){
    num1 -= num2
    return num1
}
function multiply(num1,num2){
   num1 *= num2
   return num1
}
function divide(num1,num2){
    num1 /= num2
    return num1
}